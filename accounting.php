							This course is a study of federal income tax laws related to partnerships, corporations, and fiduciaries. A study of property transactions is continued. Students are introduced to tax case research and the tax software for businesses. It is recommended that this course be taken for credit only and for audit purposes.This course focuses on the fundamental accounting concepts, principles, and procedures that make up the theoretical underpinning of financial accounting.  In addition, emphasis is placed on knowledge needed for the CPA exam. Topics reviewed will include structure of financial statements, depreciation methods, inventory valuation, revenue recognition, fair value accounting, liabilities, pensions, leases, and taxes. This course is NOT recommended for auditThe course focuses on the role of management accounting information systems in planning, controlling, evaluating performance and decision-making. The course covers traditional and contemporary concepts and techniques of product and service costing, budgeting, and interpretation of internal reported information.<hr/>
