							<tr><h2>Get Started with E-Varsity</h2></tr><br/> 
							<tr><h3>Getting Started with OCW</h3></tr><br/>
							<tr><b><u>What is OCW?</u><b></tr><br/><br/>
							<tr>OCW is a free and open publication of material from thousands of free courses, covering the entire  curriculum. That's every  department and degree program, and ranging from the introductory to the most advanced graduate level. Each OCW course includes a syllabus, some instructional material (such as lecture notes or a reading list), and some learning activities (such as assignments or exams). Many courses also have complete video lectures, free online textbooks, and faculty teaching insights. While some OCW content is custom-created for online use, most of it comes straight from the MIT classroom</tr><br/><br/>
							<tr><b><u>How do I use OCW?</u></b></tr>
							<tr><p>There's no signup, no enrollment, and no start or end dates. The entire OCW collection is always here for you. Freely browse and use it at your own pace.</p>
							<p>Knowledge is your reward. We don't offer credit or certificates for using OCW. Instead, use OCW to guide your own life-long learning, or use OCW to teach others</p>
							<p>OCW is made for sharing. Download and save files and complete courses for later. Send them to friends and colleagues. Modify or remix OCW content for any non-commercial use (just remember to credit OCW as the source). It's all good, per the terms of our Creative Commons license.</p>
							</tr><br/>
							<tr><h2>For Learning</h2></tr><br/>
